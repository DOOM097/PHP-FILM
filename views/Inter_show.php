                        За гранью возможного: для съёмок «Интерстеллара» камеры закрепляли на борту самолёта

                        Многие сцены фильма были сняты в формате IMAX

                        6 ноября на российские экраны выходит новый фильм Кристофера Нолана «Интерстеллар». Нолан
                        подарил зрителям трилогию о Тёмном рыцаре, триллер «Помни», фантасмагорию «Престиж» и
                        фантастический боевик «Начало». «Интерстеллар» - это история судьбоносного космического
                        путешествия, целью которого является открытие новой планеты, пригодной для жизни населения
                        Земли.

                        В основе картины лежит не оригинальная идея, а тема существования чёрных дыр и параллельных
                        измерений, предложенная физиком-теоретиком Кипом Торном. Первоначально права на картину
                        принадлежали студии Paramount, а снимать её должен был Стивен Спилберг. В 2006 году к
                        переработке черновика привлекли Джонатана Нолана – брата Кристофера Нолана. Работу предстояло
                        проделать кропотливую, ведь уместить в будущий фантастический блокбастер множество научных
                        обоснований – дело довольно сложное. Этот процесс занял продолжительное время. Параллельно с
                        подготовкой к съёмкам «Интерстеллара», Спилберг работал и над другими проектами. Времени на всё
                        не хватало и режиссёр был вынужден покинуть команду фильма.

                        В ПРОЦЕССЕ РАБОТЫ НАД ФИЛЬМОМ ОДИН ИЗ СОАВТОРОВ СЦЕНАРИЯ - ФИЗИК КИП ТОРН - СОВЕРШИЛ НЕСКОЛЬКО
                        НАУЧНЫХ ОТКРЫТИЙ.


                        Сценарий попал в руки к Кристоферу Нолану, который на тот момент работал со студией Warner
                        Brothers. Чтобы получить права на «Интерстеллар», Warner Bros. пришлось передать права на
                        софинансирование (и, соответственно, получение доли прибыли от проката) «Пятницы 13-го» и
                        «Южного парка» студии Paramount. В работе над этими и другими масштабными проектами студии
                        Warner Brothers принимали участие их партнёры из Legendary Pictures. В частности, они
                        финансировали работу над всей трилогией о «Тёмном рыцаре» Нолана. Хоть в настоящий момент эти
                        компании больше не сотрудничают, у Legendary до сих пор остаются права на такие проекты, как,
                        например, «Годзилла», «Интерстеллар» и «Человек из стали». Если в будущем Warner Bros. решат
                        снимать их продолжения (так случилось как раз с «Человеком из стали», вторая часть которого -
                        «Бэтмен против Супермэна: На заре справедливости» - выйдет в 2017 году), то вместе с затратами
                        на производство студии придётся делить с Legendary и прибыль от проката.
                        Много сцен «Интерстеллара» снята Ноланом на камеры IMAX. Режиссёр начал их использовать ещё в
                        процессе работы над «Тёмным рыцарем». Сначала - с большой осторожностью: он снимал городские
                        пейзажи и другие масштабные сцены, но сейчас Нолан превзошёл самого себя. Теперь, чтобы достичь
                        максимального эффекта присутствия, IMAX-камеры закрепляли в том числе на самолёты и на скафандры
                        героев, к чему до этого никто не прибегал. Это обусловлено тем, что ранние модели этих камер
                        были довольно громоздкими и тяжёлыми. Компания IMAX старается идти в ногу со временем, поэтому
                        своё оборудование она совершенствует согласно потребностям режиссёров. Таким образом и Кристофер
                        Нолан смог повлиять на развитие этой востребованной технологии.


